def arytmetyka_dodaj(x_=1.,y_=1.):
    '''
    *oblicz sume dwoch liczb*
    '''
    return {'suma': x_+y_}
def arytmetyka_pomnoz(x_=1.,y_=1.):
    '''
    *oblicz iloczyn dwoch liczb*
    '''
    return {'iloczyn': x_*y_}
def arytmetyka_odejmij(x_=1.,y_=1.):
    '''
    *oblicz roznice dwoch liczb*
    '''
    return {'roznica': x_-y_}
def arytmetyka_podziel(x_=1.,y_=1.):
    '''
    *dzielenie dwoch liczb*
    '''
    return {'iloraz': x_/y_}